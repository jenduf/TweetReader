#import "UIFont+TweetFont.h"

@implementation UIFont (TweetFont)

+ (UIFont *)tweetFontOfSize:(CGFloat)fontSize
{
	return [UIFont fontWithName:FONT_BEBASNEUE size:fontSize];
}

@end
